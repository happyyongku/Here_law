export const loadCases = (query = "", page = 1) => {
  const size = 10; // 한 페이지에 표시할 판례 수
  const startIndex = (page - 1) * size;
  const endIndex = startIndex + size;

  const totalResults = caseData.length;
  const casesForPage = caseData.slice(startIndex, endIndex);

  return {
    type: "LOAD_CASES",
    payload: {
      totalResults: totalResults,
      currentPage: page, // 현재 페이지
      totalPages: Math.ceil(totalResults / size), // 총 페이지 수
      cases: casesForPage, // 현재 페이지의 판례 데이터
    },
  };
};

